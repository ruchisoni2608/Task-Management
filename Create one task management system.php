Create one task management system.
There are 3 user roles: Super admin, Manager, Staff.
Super admin can have access to all the different modules.
Manager can create tasks, assign tasks to different staff members.
Staff members can see their tasks, set different status of the task, and record the start and end time of the task.
Admin can see the list of tasks each employee has done, along with time taken for the same, based on the date /  date range selected.


